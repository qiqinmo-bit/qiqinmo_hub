# encoding: utf-8
import os, json

base = r"D:\Codex\日常灵感工作流"

def wf(path, content):
    full = os.path.join(base, path)
    os.makedirs(os.path.dirname(full), exist_ok=True)
    with open(full, "w", encoding="utf-8") as f:
        f.write(content.lstrip("\n"))
    print("OK:", path)

